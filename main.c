#include <stdio.h>
#include <ctype.h>
#include <string.h>

// LEXICAL ANALYZER PART
/* Variables */
int charClass;
char lexeme [100];
char nextChar;
int lexLen;
int token;
int nextToken;
FILE *in_fp, *fopen();
char *line = NULL;
size_t line_len = 0;
char *line_stream = NULL;



/* Function declarations */
void addChar();
void getChar();
void getNonBlank();
int lex();

/* These functions return 1 if there is no error, returns 0 if there error occurs*/
int stmt();
int expr();
int term();
int factor();
void error();

/* Character classes */
#define LETTER 0
#define DIGIT 1
#define UNKNOWN 99
/* Token codes */
#define INT_LIT 10
#define IDENT 11
#define ASSIGN_OP 20
#define ADD_OP 21
#define SUB_OP 22
#define MULT_OP 23
#define DIV_OP 24
#define LEFT_PAREN 25
#define RIGHT_PAREN 26

/* main driver */
void main(int argc, const char * argv[]) {
    /* Open the input data file and process its contents */
    int count = 0;
    if ((in_fp = fopen(argv[1], "r")) == NULL)
        printf("ERROR - cannot open the text file \n");
    else {
        // For each line
        while(getline(&line, &line_len, in_fp)!= -1){
            line_stream = fmemopen(line, strlen(line), "r");
            getChar();
            lex();
            if(nextToken != EOF){
                stmt();
            }
        }
    }
}

int lookup(char ch) {
    switch (ch) {
        case '=':
            addChar();
            nextToken = ASSIGN_OP;
            break;
        case '(':
            addChar();
            nextToken = LEFT_PAREN;
            break;
        case ')':
            addChar();
            nextToken = RIGHT_PAREN;
            break;
        case '+':
            addChar();
            nextToken = ADD_OP;
            break;
        case '-':
            addChar();
            nextToken = SUB_OP;
            break;
        case '*':
            addChar();
            nextToken = MULT_OP;
            break;
        case '/':
            addChar();
            nextToken = DIV_OP;
            break;
        default:
            addChar();
            nextToken = EOF;
            break;
    }
    return nextToken;
}

/* addChar - a function to add nextChar to lexeme */
void addChar() {
    if (lexLen <= 98) {
        lexeme[lexLen++] = nextChar;
        lexeme[lexLen] = 0;
    }
    else
    printf("Error - lexeme is too long \n");
}

/* getChar - a function to get the next character of
input and determine its character class */
void getChar() {
    if ((nextChar = getc(line_stream)) != EOF) {
        if (isalpha(nextChar))
            charClass = LETTER;
        else if (isdigit(nextChar))
            charClass = DIGIT;
        else 
            charClass = UNKNOWN;
    }
    else
        charClass = EOF;
}

/* getNonBlank - a function to call getChar until it returns a non- whitespace character */
void getNonBlank() {
    while (isspace(nextChar))
    getChar();
}

/* lex - a simple lexical analyzer for arithmetic expressions */
int lex() {
    lexLen = 0;
    getNonBlank();
    switch (charClass) {
        /* Parse identifiers */
        case LETTER:
            addChar();
            getChar();
            while (charClass == LETTER || charClass == DIGIT) {
                addChar();
                getChar();
            }
            nextToken = IDENT;
            break;
        /* Parse integer literals */
        case DIGIT:
            addChar();
            getChar();
            while (charClass == DIGIT) {
                addChar();
                getChar();
            }
            nextToken = INT_LIT;
            break;
        /* Parentheses and operators */
        case UNKNOWN:
            lookup(nextChar);
            getChar();
            break;
        /* EOF */
        case EOF:
            nextToken = EOF;
            lexeme[0] = 'E';
            lexeme[1] = 'O';
            lexeme[2] = 'F';
            lexeme[3] = 0;
            break;
    } /* End of switch */
    printf("Next token is: %d, Next lexeme is %s\n",nextToken, lexeme);
    return nextToken;
} 

//SYNTAX ANALYZER PART

/*Parses strings in the language generated by the rule: <stmt> -> id = <expr> */
int stmt(){
    printf("Enter <stmt>\n");
    if(nextToken == IDENT){
        lex();
        if(nextToken == ASSIGN_OP){
            lex();
            if(expr()==0){
                return 0;
            }
        }
        else{
            error();
            return 0;
        }
    }
    else{
        error();
        return 0;
    }
    printf("Exit <stmt>\n");
    return 1;
}

/*Parses strings in the language generated by the rule: <expr> -> <term> {(+ | -) <term>} */
int expr() {
    printf("Enter <expr>\n");
    /* Parse the first term */
    if(term()==0){
        return 0;
    };
    /* As long as the next token is + or -, get the next token and parse the next term */
    while (nextToken == ADD_OP || nextToken == SUB_OP) {
        lex();
        if(term()==0){
            return 0;
        };
    }
    printf("Exit <expr>\n");
    return 1;
} 

/*Parses strings in the language generated by the rule: <term> -> <factor> {(* | /) <factor>) */
int term() {
printf("Enter <term>\n");
    /* Parse the first factor */
    if(factor()==0){
        return 0;
    };
    /* As long as the next token is * or /, get the next token and parse the next factor */
    while (nextToken == MULT_OP || nextToken == DIV_OP) {
        lex();
        if(factor()==0){
            return 0;
        };
    }
    printf("Exit <term>\n");
    return 1;
} 

/* factor
Parses strings in the language generated by the rule:
<factor> -> id | int_constant | ( <expr )
*/
int factor() {
    printf("Enter <factor>\n");
    /* Determine which RHS */
    if (nextToken == IDENT || nextToken == INT_LIT)
    /* Get the next token */
        lex();
    /* If the RHS is ( <expr>), call lex to pass over the left parenthesis, call expr, and check for the right parenthesis */
    else {
        if (nextToken == LEFT_PAREN) {
            lex();
            expr();
            if (nextToken == RIGHT_PAREN)
                lex();
            else
                error();
                return 0;
        } 
        else
            error();
            return 0;
    } 
    printf("Exit <factor>\n");
    return 1;
} 

void error() {
  printf("Error\n");
}